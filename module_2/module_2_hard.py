#В первом поле камни с числом менялись постоянно (от 3 до 20) случайным образом
def generate_passwords():
    for n in range(3, 21):  # Цикл от 3 до 20
        result = ""  # Инициализируем пустую строку для пароля
        for a in range(1, n + 1):  # Перебираем первое число от 1 до n
            for b in range(1, n + 1):  # Перебираем второе число от 1 до n
                sum_ab = a + b  # Считаем сумму a и b
                if n % sum_ab == 0:  # Проверяем, делится ли n на сумму
                    result += f"{a}{b}"  # Добавляем пару в результат
        print(f"{n} - {result}")  # Выводим число n и полученный пароль

# Запускаем функцию
generate_passwords()

